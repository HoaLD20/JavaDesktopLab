    public void initThread() {
        thread = new Thread(new Runnable() {
            @Override
            public void run() {
                while (process != 100) {
                    try {
                        process += 1;
                        jProgressBar1.setValue(process);
                        lbProcess.setText("Operation is " + process + "% complete");
                        thread.sleep(500);
                    } catch (InterruptedException ex) {
                        ex.printStackTrace();
                    }
                }
            }
        });
    }
    
